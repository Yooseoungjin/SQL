FROM BANK.EMP
WHERE DEPTNO = 10
UNION
SELECT EMPNO,ENAME,SAL,DEPTNO 
FROM BANK.EMP
WHERE DEPTNO = 20;

SELECT EMPNO,ENAME,SAL,DEPTNO 
FROM BANK.EMP
WHERE LENGTH(ENAME) >= 5;

