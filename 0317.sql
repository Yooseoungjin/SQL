FROM DUAL;

SELECT SUBSTR(JOB,-LENGTH(JOB),2) 
FROM BANK.EMP

SELECT TRIM('_ _ORACLE_ _') AS TRIM
,TRIM(LEADING FROM'_ _ORACLE_ _') AS TRIM_LEADING
FROM DUAL;

